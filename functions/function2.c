/*. Explique a diferença entre Variáveis Locais e Variáveis Globais.
*/

/*Variáveis locais são aquelas que só têm validade dentro do bloco no qual são
declaradas. Essas podem ser declaradas dentro de uma função ou da Main, por exemplo. Já as variáveis globais são declaradas fora de todas as funções do programa e consequentemente podem ser alteradas por todas as funções do programa, embora o uso de variáveis globais não seja recomendado para todos os casos.*/ 